ctxt VectorProgram::computation(std::vector<ctxt> ts, std::map<std::string, ptxt> bits, RuntimeContext &info)
{
    seal::RelinKeys rk = info.keys->rk;
    seal::GaloisKeys gk = info.keys->gk;

    ctxt vs[7];
    ctxt ss[16];

    vs[0] = ts[0]; // vector load instr
    info.eval->rotate_rows(vs[0], -16, gk, ss[0]); // __s0 = __v0 >> 16
    info.eval->rotate_rows(vs[0], -40, gk, ss[1]); // __s1 = __v0 >> 40
    info.eval->rotate_rows(vs[0], -18, gk, ss[2]); // __s2 = __v0 >> 18
    info.eval->rotate_rows(vs[0], -19, gk, ss[3]); // __s3 = __v0 >> 19
    info.eval->rotate_rows(vs[0], -34, gk, ss[4]); // __s4 = __v0 >> 34
    info.eval->rotate_rows(vs[0], -37, gk, ss[5]); // __s5 = __v0 >> 37
    info.eval->rotate_rows(vs[0], -23, gk, ss[6]); // __s6 = __v0 >> 23
    info.eval->rotate_rows(vs[0], -39, gk, ss[7]); // __s7 = __v0 >> 39
    vs[1] = ts[2]; // vector load instr
    info.eval->rotate_rows(vs[1], -32, gk, ss[8]); // __s8 = __v1 >> 32
    info.eval->rotate_rows(vs[1], -2, gk, ss[9]); // __s9 = __v1 >> 2
    info.eval->rotate_rows(vs[1], -34, gk, ss[10]); // __s10 = __v1 >> 34
    info.eval->rotate_rows(vs[1], -35, gk, ss[11]); // __s11 = __v1 >> 35
    info.eval->rotate_rows(vs[1], -39, gk, ss[12]); // __s12 = __v1 >> 39
    info.eval->rotate_rows(vs[1], -21, gk, ss[13]); // __s13 = __v1 >> 21
    info.eval->rotate_rows(vs[1], -23, gk, ss[14]); // __s14 = __v1 >> 23
    info.eval->rotate_rows(vs[1], -25, gk, ss[15]); // __s15 = __v1 >> 25
    
    // __t4 = blend(__s1@1000000000000000010000000000000000000000000, __v0@0001000000000000000010000000000000000000000, __s5@0000010000000100000000000000000000000000000, __s4@0000001000100000000000000000000000000000000, __s7@0000000000000000100000000000000000000000000, __s0@0000000000000000000100000000000100000000000, __s2@0000000000000000000001000000010001000100000, __s3@0000000000000000000000100000001000000001000, __s6@0000000000000000000000000000000000100010000)
    ctxt t4_1, t4_2, t4_3, t4_4, t4_5, t4_6, t4_7, t4_8, t4_9;
    info.eval->multiply_plain(ss[1], bits["1000000000000000010000000000000000000000000"], t4_1);
    info.eval->multiply_plain(vs[0], bits["0001000000000000000010000000000000000000000"], t4_2);
    info.eval->multiply_plain(ss[5], bits["0000010000000100000000000000000000000000000"], t4_3);
    info.eval->multiply_plain(ss[4], bits["0000001000100000000000000000000000000000000"], t4_4);
    info.eval->multiply_plain(ss[7], bits["0000000000000000100000000000000000000000000"], t4_5);
    info.eval->multiply_plain(ss[0], bits["0000000000000000000100000000000100000000000"], t4_6);
    info.eval->multiply_plain(ss[2], bits["0000000000000000000001000000010001000100000"], t4_7);
    info.eval->multiply_plain(ss[3], bits["0000000000000000000000100000001000000001000"], t4_8);
    info.eval->multiply_plain(ss[6], bits["0000000000000000000000000000000000100010000"], t4_9);
    info.eval->add_many({t4_1, t4_2, t4_3, t4_4, t4_5, t4_6, t4_7, t4_8, t4_9}, ts[4]);
    
    
    // __t5 = blend(__s13@1000000000000000010010000000000000000000000, __s8@0001000000000000000000000000000000000000000, __s10@0000010000000100000000000000001000000000000, __s11@0000001000000000000000000000010100100000000, __s12@0000000000100000000000000000000001000010000, __s9@0000000000000000100000000000000000000001000, __s15@0000000000000000000101000000000000000000000, __s14@0000000000000000000000100000000000000000000, __v1@0000000000000000000000000000000000000100000)
    ctxt t5_1, t5_2, t5_3, t5_4, t5_5, t5_6, t5_7, t5_8, t5_9;
    info.eval->multiply_plain(ss[13], bits["1000000000000000010010000000000000000000000"], t5_1);
    info.eval->multiply_plain(ss[8], bits["0001000000000000000000000000000000000000000"], t5_2);
    info.eval->multiply_plain(ss[10], bits["0000010000000100000000000000001000000000000"], t5_3);
    info.eval->multiply_plain(ss[11], bits["0000001000000000000000000000010100100000000"], t5_4);
    info.eval->multiply_plain(ss[12], bits["0000000000100000000000000000000001000010000"], t5_5);
    info.eval->multiply_plain(ss[9], bits["0000000000000000100000000000000000000001000"], t5_6);
    info.eval->multiply_plain(ss[15], bits["0000000000000000000101000000000000000000000"], t5_7);
    info.eval->multiply_plain(ss[14], bits["0000000000000000000000100000000000000000000"], t5_8);
    info.eval->multiply_plain(vs[1], bits["0000000000000000000000000000000000000100000"], t5_9);
    info.eval->add_many({t5_1, t5_2, t5_3, t5_4, t5_5, t5_6, t5_7, t5_8, t5_9}, ts[5]);
    
    info.eval->sub(ts[4], ts[5], vs[2]); // __v2 = __t4 - __t5
    
    // __t6 = blend(__s1@1000000000000000000000000000000000000000000, __s4@0010000000000000000000000000000000000000000, __s7@0000000000010000100000000000000000000000000, __s5@0000000000000110000000000000000000000000000, __v0@0000000000000000000010000000000000000000000, __s2@0000000000000000000000000000010000000000000, __s0@0000000000000000000000000000000000010000000, __s6@0000000000000000000000000000000000000010001, __s3@0000000000000000000000000000000000000001000)
    ctxt t6_1, t6_2, t6_3, t6_4, t6_5, t6_6, t6_7, t6_8, t6_9;
    info.eval->multiply_plain(ss[1], bits["1000000000000000000000000000000000000000000"], t6_1);
    info.eval->multiply_plain(ss[4], bits["0010000000000000000000000000000000000000000"], t6_2);
    info.eval->multiply_plain(ss[7], bits["0000000000010000100000000000000000000000000"], t6_3);
    info.eval->multiply_plain(ss[5], bits["0000000000000110000000000000000000000000000"], t6_4);
    info.eval->multiply_plain(vs[0], bits["0000000000000000000010000000000000000000000"], t6_5);
    info.eval->multiply_plain(ss[2], bits["0000000000000000000000000000010000000000000"], t6_6);
    info.eval->multiply_plain(ss[0], bits["0000000000000000000000000000000000010000000"], t6_7);
    info.eval->multiply_plain(ss[6], bits["0000000000000000000000000000000000000010001"], t6_8);
    info.eval->multiply_plain(ss[3], bits["0000000000000000000000000000000000000001000"], t6_9);
    info.eval->add_many({t6_1, t6_2, t6_3, t6_4, t6_5, t6_6, t6_7, t6_8, t6_9}, ts[6]);
    
    
    // __t7 = blend(__s13@1000000000000000000010000000000000000000000, __s14@0010000000000000000000000000000000000000000, __s8@0000000000010000000000000000000000000000000, __s10@0000000000000100000000000000000000000000000, __s11@0000000000000010000000000000010000000000000, __s9@0000000000000000100000000000000000000001000, __s12@0000000000000000000000000000000000010010000, __v1@0000000000000000000000000000000000000000001)
    ctxt t7_1, t7_2, t7_3, t7_4, t7_5, t7_6, t7_7, t7_8;
    info.eval->multiply_plain(ss[13], bits["1000000000000000000010000000000000000000000"], t7_1);
    info.eval->multiply_plain(ss[14], bits["0010000000000000000000000000000000000000000"], t7_2);
    info.eval->multiply_plain(ss[8], bits["0000000000010000000000000000000000000000000"], t7_3);
    info.eval->multiply_plain(ss[10], bits["0000000000000100000000000000000000000000000"], t7_4);
    info.eval->multiply_plain(ss[11], bits["0000000000000010000000000000010000000000000"], t7_5);
    info.eval->multiply_plain(ss[9], bits["0000000000000000100000000000000000000001000"], t7_6);
    info.eval->multiply_plain(ss[12], bits["0000000000000000000000000000000000010010000"], t7_7);
    info.eval->multiply_plain(vs[1], bits["0000000000000000000000000000000000000000001"], t7_8);
    info.eval->add_many({t7_1, t7_2, t7_3, t7_4, t7_5, t7_6, t7_7, t7_8}, ts[7]);
    
    info.eval->sub(ts[6], ts[7], vs[3]); // __v3 = __t6 - __t7
    
    // __t8 = blend(__v3@1000000000000100000010000000010000000010000, __v2@0000000000000000100000000000000000000001000)
    ctxt t8_1, t8_2;
    info.eval->multiply_plain(vs[3], bits["1000000000000100000010000000010000000010000"], t8_1);
    info.eval->multiply_plain(vs[2], bits["0000000000000000100000000000000000000001000"], t8_2);
    info.eval->add(t8_1, t8_2, ts[8]);
    
    
    // __t9 = blend(__v2@1000000000000100000010000000010000000010000, __v3@0000000000000000100000000000000000000001000)
    ctxt t9_1, t9_2;
    info.eval->multiply_plain(vs[2], bits["1000000000000100000010000000010000000010000"], t9_1);
    info.eval->multiply_plain(vs[3], bits["0000000000000000100000000000000000000001000"], t9_2);
    info.eval->add(t9_1, t9_2, ts[9]);
    
    info.eval->multiply(ts[8], ts[9], vs[4]); // __v4 = __t8 * __t9
    info.eval->relinearize_inplace(vs[4], rk);
    
    // __t10 = blend(__s4@0010001000100000000000000000000000000000000, __v0@0001000000000000000000000000000000000000000, __s5@0000010000000010000000000000000000000000000, __s7@0000000000010000000000000000000000000000000, __s1@0000000000000000010000000000000000000000000, __s0@0000000000000000000100000000000100010000000, __s2@0000000000000000000001000000000001000100000, __s3@0000000000000000000000100000001000000000000, __s6@0000000000000000000000000000000000100000001)
    ctxt t10_1, t10_2, t10_3, t10_4, t10_5, t10_6, t10_7, t10_8, t10_9;
    info.eval->multiply_plain(ss[4], bits["0010001000100000000000000000000000000000000"], t10_1);
    info.eval->multiply_plain(vs[0], bits["0001000000000000000000000000000000000000000"], t10_2);
    info.eval->multiply_plain(ss[5], bits["0000010000000010000000000000000000000000000"], t10_3);
    info.eval->multiply_plain(ss[7], bits["0000000000010000000000000000000000000000000"], t10_4);
    info.eval->multiply_plain(ss[1], bits["0000000000000000010000000000000000000000000"], t10_5);
    info.eval->multiply_plain(ss[0], bits["0000000000000000000100000000000100010000000"], t10_6);
    info.eval->multiply_plain(ss[2], bits["0000000000000000000001000000000001000100000"], t10_7);
    info.eval->multiply_plain(ss[3], bits["0000000000000000000000100000001000000000000"], t10_8);
    info.eval->multiply_plain(ss[6], bits["0000000000000000000000000000000000100000001"], t10_9);
    info.eval->add_many({t10_1, t10_2, t10_3, t10_4, t10_5, t10_6, t10_7, t10_8, t10_9}, ts[10]);
    
    
    // __t11 = blend(__s14@0010000000000000000000100000000000000000000, __s8@0001000000010000000000000000000000000000000, __s10@0000010000000000000000000000001000000000000, __s11@0000001000000010000000000000000100100000000, __s12@0000000000100000000000000000000001010000000, __s13@0000000000000000010000000000000000000000000, __s15@0000000000000000000101000000000000000000000, __v1@0000000000000000000000000000000000000100001)
    ctxt t11_1, t11_2, t11_3, t11_4, t11_5, t11_6, t11_7, t11_8;
    info.eval->multiply_plain(ss[14], bits["0010000000000000000000100000000000000000000"], t11_1);
    info.eval->multiply_plain(ss[8], bits["0001000000010000000000000000000000000000000"], t11_2);
    info.eval->multiply_plain(ss[10], bits["0000010000000000000000000000001000000000000"], t11_3);
    info.eval->multiply_plain(ss[11], bits["0000001000000010000000000000000100100000000"], t11_4);
    info.eval->multiply_plain(ss[12], bits["0000000000100000000000000000000001010000000"], t11_5);
    info.eval->multiply_plain(ss[13], bits["0000000000000000010000000000000000000000000"], t11_6);
    info.eval->multiply_plain(ss[15], bits["0000000000000000000101000000000000000000000"], t11_7);
    info.eval->multiply_plain(vs[1], bits["0000000000000000000000000000000000000100001"], t11_8);
    info.eval->add_many({t11_1, t11_2, t11_3, t11_4, t11_5, t11_6, t11_7, t11_8}, ts[11]);
    
    info.eval->sub(ts[10], ts[11], vs[5]); // __v5 = __t10 - __t11
    
    // __t12 = blend(__v5@0011010000100010000101100000000001000100000, __v2@0000001000000000010000000000001100100000000, __v3@0000000000010000000000000000000000010000001)
    ctxt t12_1, t12_2, t12_3;
    info.eval->multiply_plain(vs[5], bits["0011010000100010000101100000000001000100000"], t12_1);
    info.eval->multiply_plain(vs[2], bits["0000001000000000010000000000001100100000000"], t12_2);
    info.eval->multiply_plain(vs[3], bits["0000000000010000000000000000000000010000001"], t12_3);
    info.eval->add_many({t12_1, t12_2, t12_3}, ts[12]);
    
    
    // __t13 = blend(__v3@0010000000000010000000000000000000000000000, __v2@0001010000100000000101100000000001000100000, __v5@0000001000010000010000000000001100110000001)
    ctxt t13_1, t13_2, t13_3;
    info.eval->multiply_plain(vs[3], bits["0010000000000010000000000000000000000000000"], t13_1);
    info.eval->multiply_plain(vs[2], bits["0001010000100000000101100000000001000100000"], t13_2);
    info.eval->multiply_plain(vs[5], bits["0000001000010000010000000000001100110000001"], t13_3);
    info.eval->add_many({t13_1, t13_2, t13_3}, ts[13]);
    
    info.eval->multiply(ts[12], ts[13], vs[6]); // __v6 = __t12 * __t13
    info.eval->relinearize_inplace(vs[6], rk);
    return vs[6];
}
    